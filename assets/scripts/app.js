function toggle(){
    let col = document.querySelectorAll('.price_col');
    let cont = document.querySelector('.question_cont');
    let close = document.querySelector('.question_cont .close');
    let img = document.querySelector(".img");
    let plus_wrap = document.querySelector('.plus_wrap');
    col.forEach(element => {
        element.addEventListener('click',function(){
            if(cont.classList.contains('active_block')){
                cont.classList.remove('active_block');
            }else{
                cont.classList.add('active_block');
            }
        })
    });
    close.addEventListener('click',function(){
        if(cont.classList.contains('active_block')){
            cont.classList.remove('active_block');
            let answ = document.querySelector('.name_wrap answ');
            answ.innerHTML = " ";
            answ.style.color = 'white';
            img.style.background = "none";
            
            plus_wrap.style.display = 'none';
        }else{
            cont.classList.add('active_block');
        }
    })
    

}
toggle();




